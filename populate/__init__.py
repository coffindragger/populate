def random_date(days_give=120, days_take=-120, today=None):
    if today is None:
        today = datetime.datetime.today() 
    return today + datetime.timedelta(days=random.randint(days_take,days_give))

def random_time():
    return datetime.time(random.randint(0,23), random.randint(0,59))



def strip_punctuation(str):
    return str.translate(string.maketrans("",""), string.punctuation)


